package pojo;

public class Abszolut {
	private String nev;
	private int rajt;
	private String kategoria;
	private int potkocsis;
	private int szlalom;
	private int gyorsulas;
	private float osszes;

	public Abszolut() {

	}

	public String getNev() {
		return nev;
	}

	public void setNev(String nev) {
		this.nev = nev;
	}

	public int getRajt() {
		return rajt;
	}

	public void setRajt(int rajt) {
		this.rajt = rajt;
	}

	public String getKategoria() {
		return kategoria;
	}

	public void setKategoria(String kategoria) {
		this.kategoria = kategoria;
	}

	public int getPotkocsis() {
		return potkocsis;
	}

	public void setPotkocsis(int potkocsis) {
		this.potkocsis = potkocsis;
	}

	public int getSzlalom() {
		return szlalom;
	}

	public void setSzlalom(int szlalom) {
		this.szlalom = szlalom;
	}

	public int getGyorsulas() {
		return gyorsulas;
	}

	public void setGyorsulas(int gyorsulas) {
		this.gyorsulas = gyorsulas;
	}

	public float getOsszes() {
		return osszes;
	}

	public void setOsszes(float osszes) {
		this.osszes = osszes;
	}
}
