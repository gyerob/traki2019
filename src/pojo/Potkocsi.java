package pojo;

public class Potkocsi {
	private String nev;
	private int rajt;
	private String neme;
	private String kategoria;
	private String ido;
	private int hiba;
	private String vido;

	public Potkocsi() {

	}

	public String getNev() {
		return nev;
	}

	public void setNev(String nev) {
		this.nev = nev;
	}

	public int getRajt() {
		return rajt;
	}

	public void setRajt(int rajt) {
		this.rajt = rajt;
	}

	public String getNeme() {
		return neme;
	}

	public void setNeme(String neme) {
		this.neme = neme;
	}

	public String getKategoria() {
		return kategoria;
	}

	public void setKategoria(String kategoria) {
		this.kategoria = kategoria;
	}

	public String getIdo() {
		return ido;
	}

	public void setIdo(String ido) {
		this.ido = ido;
	}

	public int getHiba() {
		return hiba;
	}

	public void setHiba(int hiba) {
		this.hiba = hiba;
	}

	public String getVido() {
		return vido;
	}

	public void setVido(String vido) {
		this.vido = vido;
	}
}
