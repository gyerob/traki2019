package pojo;

public class Dontos {
	private String nev;
	private int rajt;
	private int nyert;

	public Dontos() {

	}

	public String getNev() {
		return nev;
	}

	public int getRajt() {
		return rajt;
	}

	public void setNev(String name) {
		this.nev = name;
	}

	public void setRajt(int number) {
		this.rajt = number;
	}

	public int getNyert() {
		return nyert;
	}

	public void setNyert(int nyert) {
		this.nyert = nyert;
	}
}
